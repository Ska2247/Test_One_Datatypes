package com.tvs.testing;

public class Test_One_Datatypes {

	public static void main(String[] args) {

		byte a = 45;
		System.out.println("the value of byte = " + a);
		short b = 7896;
		System.out.println("the value of short = " + b);
		int c = 56785498;
		System.out.println("the value of int = " + c);
		long d = 123456789012345l;
		System.out.println("the value of long = " + d);
		float e = 0.1234567f;
		System.out.println("the value of float = " + e);
		double f = 0.12345678909876;
		System.out.println("the value of double = " + f);
		char g = '#';
		System.out.println("the value of char = " + g);
		boolean h = true;
		System.out.println("the value of boolean = " + h);
		String i = "cute cat sitting on the table";
		System.out.println("the value of String = " + i);
	}
}
